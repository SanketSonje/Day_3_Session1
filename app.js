// app.js
function add(a, b) {
  return a + b;
}

function multiply(x, y) {
  return x * y;
}

let num1 = 5;
let num2 = 10;

console.log("Starting program...");

let sum = add(num1, num2);
console.log("Sum:", sum);

let product = multiply(num1, num2);
console.log("Product:", product);

console.log("Program finished.");
